package Loops;

public class Lab001_For_with_If {
    public static void main(String[] args) {
        for(int i=0;i<18;i++)
        {
            if(i>15)
            {
                System.out.println("Print1");
            }
            else
            {
                System.out.println("Print2");
            }
        }
    }



}


//16 times it will print Print2
//2 times it will print Print1
