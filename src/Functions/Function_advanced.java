package Functions;

public class Function_advanced {
    public static void main(String[] args) {

        int result1= sum_function(3,6);
        int result2= sum_function(3,6);


    }

    static int sum_function(int a,int b)
    {
        return a+b;
    }

    static int subtract_function(int a,int b)
    {
        return a-b;
    }

    static int mul_function(int a,int b)
    {
        return a*b;
    }

    static int div_function(int a,int b)
    {
        return a/b;
    }

    static int mod_function(int a,int b)
    {
        return a%b;
    }





}
